<?php
    // while loop

    // starting point
    // condition/ending point
    // increment


    // starting point
    $x = 0;

    // condition/ending point
    while ($x < 10) {
        echo $x;
        // increment
        ++$x;
    }

    echo "<br>";

    //do while
    $y = 20;
    do{
        echo $y;
        $y++;
    }while($y < 10);

    echo "<br>";

    //for loop
    for($i = 0; $i < 10; $i++){
        echo $i;
    }
?>